# List Methods
c = [23, 1, 55, 254, 4, 54]
c.sort() # Sorts the Original Lists
print(c)
c.reverse() # Reverses the lists
print(c)
c.append(49) # Adds input at the end of the lists
print(c)
c.insert(2, 323) # Inserts the 323 input at 1 index.
print(c)
c.remove(254) # Remove 254 from the lists
print(c)
